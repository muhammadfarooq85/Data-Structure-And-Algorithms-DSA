// Waht is Programming?

// Programming is the process of creating a set of instructions that tell a computer how to perform a task.
// It involves writing code in a programming language, which can be executed by a computer to perform specific operations.

// What are flow charts?

// Flow charts are visual representations of a process or algorithm. They use various shapes to denote different types of actions or steps in a process.
// Flow charts help in understanding the flow of control and data in a program, making it easier to design and debug algorithms.

// What is C++?

// C++ is a high-level programming language that supports object-oriented, procedural, and generic programming paradigms.
// It is widely used for system/software development, game development, and performance-critical applications.

// What is pesudo code?

// Pseudo code is an informal high-level description of a computer program or algorithm. It uses the structural conventions of programming languages but is intended for human reading rather than machine reading.
// Pseudo code helps programmers plan and discuss algorithms without getting bogged down in syntax details of a specific programming language.

